// Function to change language
function changeLanguage(langCode) {
    if (!translations[langCode]) {
        console.log('Language not supported:', langCode);
        return;
    }
    
    const t = translations[langCode];
    
    // Update title
    document.querySelector('h1').textContent = t.title;
    
    // Update questions
    const questions = document.querySelectorAll('.question h3');
    if (questions.length >= 12) {
        questions[0].textContent = t.age_question;
        questions[1].textContent = t.digital_assistant_question;
        questions[2].textContent = t.tech_difficulty_question;
        questions[3].textContent = t.ai_companion_question;
        questions[4].textContent = t.helpful_features_question;
        questions[5].textContent = t.trial_question;
        questions[6].textContent = t.voice_comfort_question;
        questions[7].textContent = t.concerns_question;
        questions[8].textContent = t.payment_question;
        questions[9].textContent = t.interest_question;
        questions[10].textContent = t.comments_question;
        questions[11].textContent = t.contact_question;
    }
    
    // Update subtitles
    const subtitles = document.querySelectorAll('.question p');
    if (subtitles.length >= 4) {
        subtitles[0].textContent = t.ai_companion_subtitle;
        subtitles[1].textContent = t.helpful_features_subtitle;
        subtitles[2].textContent = t.voice_comfort_subtitle;
        subtitles[3].textContent = t.concerns_subtitle;
    }
    
    // Update labels
    const labels = document.querySelectorAll('label');
    labels.forEach(label => {
        const forAttr = label.getAttribute('for');
        if (forAttr) {
            switch(forAttr) {
                case 'age_60_69': label.textContent = t.age_60_69; break;
                case 'age_70_79': label.textContent = t.age_70_79; break;
                case 'age_80_89': label.textContent = t.age_80_89; break;
                case 'age_90_plus': label.textContent = t.age_90_plus; break;
                case 'digital_yes': label.textContent = t.yes; break;
                case 'digital_no': label.textContent = t.no; break;
                case 'difficulty_easy': label.textContent = t.very_easy; break;
                case 'difficulty_somewhat': label.textContent = t.somewhat_easy; break;
                case 'difficulty_neutral': label.textContent = t.neutral; break;
                case 'difficulty_hard': label.textContent = t.somewhat_difficult; break;
                case 'difficulty_very_hard': label.textContent = t.very_difficult; break;
                case 'interest_very': label.textContent = t.yes_very_much; break;
                case 'interest_somewhat': label.textContent = t.maybe_sounds_interesting; break;
                case 'interest_neutral': label.textContent = t.not_sure; break;
                case 'interest_not': label.textContent = t.probably_not; break;
                case 'feature_reminders': label.textContent = t.medicine_reminders; break;
                case 'feature_emergency': label.textContent = t.emergency_calls; break;
                case 'feature_conversation': label.textContent = t.conversation; break;
                case 'feature_weather': label.textContent = t.weather_updates; break;
                case 'feature_news': label.textContent = t.news_updates; break;
                case 'feature_appointments': label.textContent = t.appointment_reminders; break;
                case 'feature_family': label.textContent = t.family_communication; break;
                case 'trial_yes': label.textContent = t.yes; break;
                case 'trial_no': label.textContent = t.no; break;
                case 'trial_maybe': label.textContent = t.maybe; break;
                case 'comfort_very': label.textContent = t.fine_with_it; break;
                case 'comfort_somewhat': label.textContent = t.could_get_used_to_it; break;
                case 'comfort_neutral': label.textContent = t.not_sure; break;
                case 'comfort_uncomfortable': label.textContent = t.might_feel_strange; break;
                case 'concern_privacy': label.textContent = t.privacy; break;
                case 'concern_cost': label.textContent = t.cost; break;
                case 'concern_complexity': label.textContent = t.too_complex; break;
                case 'concern_reliability': label.textContent = t.reliability; break;
                case 'concern_none': label.textContent = t.no_concerns; break;
                case 'pay_free': label.textContent = t.free; break;
                case 'pay_10': label.textContent = t.pay_1_10; break;
                case 'pay_25': label.textContent = t.pay_11_25; break;
                case 'pay_50': label.textContent = t.pay_26_50; break;
                case 'pay_more': label.textContent = t.pay_50_plus; break;
                case 'interest_1': label.textContent = t.not_interested_at_all; break;
                case 'interest_2': label.textContent = t.slightly_interested; break;
                case 'interest_3': label.textContent = t.somewhat_interested; break;
                case 'interest_4': label.textContent = t.interested; break;
                case 'interest_5': label.textContent = t.very_interested; break;
                case 'pref_email': label.textContent = t.email_label; break;
                case 'pref_phone': label.textContent = t.phone_label; break;
                case 'pref_either': label.textContent = t.either_is_fine; break;
            }
        }
    });
    
    // Update placeholders
    const placeholders = document.querySelectorAll('input[placeholder], textarea[placeholder]');
    placeholders.forEach(input => {
        const placeholder = input.getAttribute('placeholder');
        if (placeholder.includes('name')) input.placeholder = t.name_placeholder;
        else if (placeholder.includes('email')) input.placeholder = t.email_placeholder;
        else if (placeholder.includes('phone')) input.placeholder = t.phone_placeholder;
        else if (placeholder.includes('anything else')) input.placeholder = t.comments_placeholder;
    });
    
    // Update submit button
    document.querySelector('button[type="submit"]').textContent = t.submit_button;
    
    // Update contact section labels
    const contactLabels = document.querySelectorAll('label[for^="contact"]');
    contactLabels.forEach(label => {
        const forAttr = label.getAttribute('for');
        if (forAttr === 'contact_name') label.textContent = t.name_label;
        else if (forAttr === 'contact_email') label.textContent = t.email_label;
        else if (forAttr === 'contact_phone') label.textContent = t.phone_label;
    });
    
    // Update contact preference label
    const contactPrefLabel = document.querySelector('label[for="contact_preference"]');
    if (contactPrefLabel) contactPrefLabel.textContent = t.contact_preference_label;
    
    // Update contact subtitle
    const contactSubtitle = document.querySelector('.question p[style*="color: #666"]');
    if (contactSubtitle) contactSubtitle.textContent = t.contact_subtitle;
    
    console.log('Language changed to:', langCode);
} 